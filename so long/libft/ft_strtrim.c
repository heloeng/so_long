#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*str;
	int		start;
	int		end;
	int		str_len;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1) - 1;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end]))
		end--;
	str_len = end - start + 1;
	str = malloc(str_len + 1);
	if (!str)
		return (NULL);
	ft_strlcpy(str, &s1[start], str_len + 1);
	return (str);
}
